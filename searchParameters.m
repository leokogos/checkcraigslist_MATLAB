function submit(~,~)
global runSearch;
runSearch = ~runSearch;

    if runSearch
        uiresume(gcbf);
    else
        close(gcbf);
    end
end 

function checkNumber(src,~)
str=get(src,'String');
    if str2double(str)<0
        set(src,'string','');
        warndlg('Input must be positive Ignoring Input');
    end
end

function n = countLines(fid)
n = 0;
tline = fgetl(fid);
while ischar(tline)
  tline = fgetl(fid);
  n = n+1;
end
end